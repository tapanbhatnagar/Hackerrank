package Codeforces;

import java.util.Scanner;

public class OmkarAndCompletion {


    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int t = scanner.nextInt();
        while(t > 0) {
            int n = scanner.nextInt();
            for (int i = 0; i < n; i++) {
                System.out.print(2+ " ");
            }
            System.out.print("\n");
            t--;
        }
    }
}
